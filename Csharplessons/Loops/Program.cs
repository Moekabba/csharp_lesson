// Loops = Iteration statements
// allow you to loop over the same logic multipe time

//while loop
// use when you want to execute code as long as condition is true
// eg as long
int a = 1;
while (a <  5)
{
    //Console.WriteLine($"while loop: {a}");
    a++;
}


// do- while 
// r

int b = 1;
do
{
    //Console.WriteLine($" do while: {b}");
    b++;
    // learn more
} while (b < 5);

// for loop 
// use this when you know how many times you want to execute code
for (int i = 0;  i < 5; i++)
{
    Console.WriteLine($"for loop: {i}");
}